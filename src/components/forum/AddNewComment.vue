<template>
  <div id="AddNewComment">
    <label for="comment">Ecrire un commentaire</label>
    <input type="text" name="comment" v-model="comment" />
    <button v-on:click="submitComment">Envoyer</button>
  </div>
</template>




<script>
import { mainAxios } from "../../../http-common";

export default {
  name: "AddNewComment",
  data: function () {
    return {
      comment: [],
    };
  },
  props: ["id"],
  methods: {
    submitComment() {
      const userId = localStorage.getItem("userId");
      const comment = {
        comment: this.comment,
        userId: userId,
        postId: this.id,
      };

      mainAxios({
        url: "/comments",
        data: comment,
        method: "POST",
      })
        .then((response) => {
          this.$emit("comment-sent", response.data);
          console.log(response);
        })
        .catch((error) => {
          console.log(error);
        });
    },
  },
};
</script>

